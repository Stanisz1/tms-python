"""
Используя модуль math, вычислите значения по следующим
формулам:
Вычисление по первой формуле:
y = a²/3 + (a² + 4)/b + √(a² + 4)/4 + √((a² + 4)³)/4
"""
import math

a = float(input("Введите значение a: "))
b = float(input("Введите значение b: "))

# Проверка деления на ноль с циклом
while b == 0:
    print("Значение b не может быть равно 0")
    b = float(input("Введите другое значение b, которое не равно 0: "))

# Оптимизация повторяющихся вычислений
a_sq = a ** 2
a_sq_plus_4 = a_sq + 4
sqrt_expr = math.sqrt(a_sq_plus_4)

# Вычисление слагаемых
term1 = a_sq / 3
term2 = a_sq_plus_4 / b
term3 = sqrt_expr / 4
term4 = (a_sq_plus_4 * sqrt_expr) / 4  # Упрощение: √(x³) = x√x

y1 = term1 + term2 + term3 + term4

print(f"""
Результат вычислений по формуле:
y = a²/3 + (a² + 4)/b + √(a² + 4)/4 + √((a² + 4)³)/4

Промежуточные значения:
a²/3 = {term1} 
(a² + 4)/b = {term2} 
√(a² + 4)/4 = {term3}  
√((a² + 4)³)/4 = {term4} 

Итоговое значение:
y = {y1}
""")