"""
Программа получает на вход два числа и находит их НОД
(наибольший общий делитель). Пример: на вход подаются числа 12
и 18, их НОД равен 6
"""

def gcd(a, b):
    """
    Функция для вычисления НОД двух чисел с использованием алгоритма Евклида.
    """
    while b:
        a, b = b, a % b
    return abs(a)

try:
    num1 = int(input("Введите первое число: "))
    num2 = int(input("Введите второе число: "))
except ValueError:
    print("Ошибка: введите целые числа!")
    exit()

result = gcd(num1, num2)

# Вывод результата
print(f"НОД чисел {num1} и {num2} равен {result}")